uint8_t gfx_get_pixel(uint8_t row, uint8_t column)
{
    uint8_t page = row / 8;
    uint8_t bit  = row % 8;

    return (frame_buffer[page][column] >> bit) & 0x01;
}

void gfx_set_pixel(uint8_t row, uint8_t column)
{

   uint8_t page = row / 8;
   uint8_t bit  = row % 8;

    frame_buffer[page][column] |= (1 << bit);

}
void gfx_clear_pixel(uint8_t row, uint8_t column)
{
      uint8_t page = row / 8;
   uint8_t bit  = row % 8;

    frame_buffer[page][column] &= ~(1 << bit);
}
